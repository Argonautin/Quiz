// Klasa Answer reprezentuje pojedyncza odpowiedz w quizie.
public class Answer
{
    public String text;
    public boolean isCorrect;

    // Konstruktor klasy Answer, ktory inicjalizuje odpowiedz.
    public Answer(String text, boolean isCorrect)
    {
        this.text = text; // Przypisanie tekstu odpowiedzi.
        this.isCorrect = isCorrect;// Okreslenie, czy odpowiedz jest poprawna.
    }
}
